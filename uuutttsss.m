% --- Executes on button press in open.
function open_Callback(hObject, eventdata, handles)
% hObject    handle to open (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

[namafile, formatfile] = uigetfile('*.png; *.bmp; *.jpeg; *.jpg', 'membuka gambar'); %memilih gambar
if formatfile == 0
    return;
end
image = imread([formatfile, namafile]);
guidata(hObject, handles);
axes(handles.axes1); 
imshow(image);
image = fullfile(formatfile, namafile);

fileinfo = imfinfo(image);
%namafile = fileinfo.Filename;
format = fileinfo.Format;
bitdepth = fileinfo.BitDepth;
uk_file = fileinfo.FileSize;
lebar = fileinfo.Width;
tinggi = fileinfo.Height;
tipewarna = fileinfo.ColorType;
wkt_modif = fileinfo.FileModDate;

set(handles.edit10,'String', formatfile);

set(handles.edit1,'String', namafile);
set(handles.edit2,'String', format);
set(handles.edit9,'String', bitdepth);
set(handles.edit3,'String', lebar);
set(handles.edit4,'String', tinggi);
set(handles.edit5,'String', uk_file);
set(handles.edit6,'String', tipewarna);
set(handles.edit7,'String', wkt_modif);

